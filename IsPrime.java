package week1.day1;

public class IsPrime {

	public static void main(String[] args) {
		int prime=1;
		boolean isPrime = true;
		for (int i = 2; i <prime;) {
			if(prime/i==0);
			{
				isPrime= false;
				break;
				
			}
			
			
		}
		
		if(isPrime) {
			System.out.println(prime  + " is a Prime number ");
		}
		else
		{
			System.out.println("Not a prime number");
		}
		
	}
	} 